public class Bai7_cacpheptoan {
    public static void main(String[] args) {
        int a, b;
        a = 5;
        b = 10;

        System.out.println(a + b);         // Cộng
        System.out.println(a - b);         // Trừ
        System.out.println(a * b);         // Nhân
        System.out.println((double) a % b);
        System.out.println((double) a / b); // Chia ra số thực (ép kiểu đng)
    }
}
